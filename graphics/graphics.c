#include "graphics.h"

static volatile u8* const VGA = (u8*)0xA0000;

void gfx_putpixel(i32 x, i32 y, u8 color) {
    if (x < 0 || y < 0 || x >= SCREEN_W || y >= SCREEN_H) return;
    VGA[y * SCREEN_W + x] = color;
}

void gfx_clear(u8 color) {
    for (i32 y = 0; y < SCREEN_H; ++y) {
        for (i32 x = 0; x < SCREEN_W; ++x) {
            VGA[y * SCREEN_W + x] = color;
        }
    }
}

void gfx_rect(i32 x, i32 y, i32 w, i32 h, u8 color) {
    for (i32 yy = 0; yy < h; ++yy) {
        for (i32 xx = 0; xx < w; ++xx) {
            gfx_putpixel(x + xx, y + yy, color);
        }
    }
}

static void glyph5x7(i32 x, i32 y, const u8 g[7], u8 color) {
    for (int r = 0; r < 7; ++r) {
        for (int c = 0; c < 5; ++c) {
            if ((g[r] >> (4 - c)) & 1) {
                gfx_putpixel(x + c, y + r, color);
            }
        }
    }
}

void gfx_char(i32 x, i32 y, char c, u8 color) {
    static const u8 blank[7] = {0,0,0,0,0,0,0};
    static const u8 A[7]={0x0E,0x11,0x11,0x1F,0x11,0x11,0x11};
    static const u8 D[7]={0x1E,0x11,0x11,0x11,0x11,0x11,0x1E};
    static const u8 K[7]={0x11,0x12,0x14,0x18,0x14,0x12,0x11};
    static const u8 O[7]={0x0E,0x11,0x11,0x11,0x11,0x11,0x0E};
    static const u8 R[7]={0x1E,0x11,0x11,0x1E,0x14,0x12,0x11};
    static const u8 S[7]={0x0F,0x10,0x10,0x0E,0x01,0x01,0x1E};
    static const u8 T[7]={0x1F,0x04,0x04,0x04,0x04,0x04,0x04};
    static const u8 a[7]={0x00,0x00,0x0E,0x01,0x0F,0x11,0x0F};
    static const u8 b[7]={0x10,0x10,0x1E,0x11,0x11,0x11,0x1E};
    static const u8 d[7]={0x01,0x01,0x0F,0x11,0x11,0x11,0x0F};
    static const u8 e[7]={0x00,0x00,0x0E,0x11,0x1F,0x10,0x0F};
    static const u8 i[7]={0x04,0x00,0x0C,0x04,0x04,0x04,0x0E};
    static const u8 k[7]={0x10,0x10,0x12,0x14,0x18,0x14,0x12};
    static const u8 l[7]={0x0C,0x04,0x04,0x04,0x04,0x04,0x0E};
    static const u8 n[7]={0x00,0x00,0x1E,0x11,0x11,0x11,0x11};
    static const u8 o[7]={0x00,0x00,0x0E,0x11,0x11,0x11,0x0E};
    static const u8 r[7]={0x00,0x00,0x16,0x19,0x10,0x10,0x10};
    static const u8 s[7]={0x00,0x00,0x0F,0x10,0x0E,0x01,0x1E};
    static const u8 t[7]={0x04,0x04,0x1F,0x04,0x04,0x04,0x03};
    static const u8 v[7]={0x00,0x00,0x11,0x11,0x11,0x0A,0x04};
    static const u8 y[7]={0x00,0x00,0x11,0x11,0x0F,0x01,0x0E};
    static const u8 zero[7]={0x0E,0x11,0x13,0x15,0x19,0x11,0x0E};
    static const u8 one[7]={0x04,0x0C,0x04,0x04,0x04,0x04,0x0E};
    static const u8 colon[7]={0x00,0x04,0x00,0x00,0x04,0x00,0x00};
    static const u8 dash[7]={0x00,0x00,0x00,0x1F,0x00,0x00,0x00};

    const u8* g = blank;
    switch (c) {
        case 'A': g=A; break; case 'D': g=D; break; case 'K': g=K; break;
        case 'O': g=O; break; case 'R': g=R; break; case 'S': g=S; break;
        case 'T': g=T; break; case 'a': g=a; break; case 'b': g=b; break;
        case 'd': g=d; break; case 'e': g=e; break; case 'i': g=i; break;
        case 'k': g=k; break; case 'l': g=l; break; case 'n': g=n; break;
        case 'o': g=o; break; case 'r': g=r; break; case 's': g=s; break;
        case 't': g=t; break; case 'v': g=v; break; case 'y': g=y; break;
        case '0': g=zero; break; case '1': g=one; break; case ':': g=colon; break;
        case '-': g=dash; break; default: g=blank; break;
    }
    glyph5x7(x, y, g, color);
}

void gfx_text(i32 x, i32 y, const char* s, u8 color) {
    while (*s) {
        gfx_char(x, y, *s++, color);
        x += 6;
    }
}
