void gfx_char(i32 x, i32 y, char c, u8 color) {
    const u8* g = select_glyph(c);
    glyph5x7(x, y, g, color);
}

void gfx_text(i32 x, i32 y, const char* s, u8 color) {
    while (*s) {
        gfx_char(x, y, *s++, color);
        x += 6;
    }
}
